#tcstype is a string with the last four digits of the TCS model number
def create_auto_discovered_children_for(parent, tcstype):
    if defined_types.has_key(tcstype):
        type_map = defined_types[tcstype]
        for parameter in type_map.keys():  #iterate through each letter code
            childDict = {}
            childDict['name'] = parameter #each is named for the letter
            childDict['parent'] = parent
            childDict['parameter']=parameter #incase the name is changed
            childDict['controller_type']=tcstype
            group = value.ParameterGroup() #one for each letter code parameter
            group.configure(childDict)
            for position in type_map[parameter].keys():
                p = position
                node_info = type_map[parameter][position] #from position table for this device type
                childDict = {}
                childDict['name'] = '%02d' % position
                if node_info[3] == holiday_type:
                    p = position - 100
                    childDict['name'] = 'H%02d' % p
                childDict['parent'] = group
                #childDict['parameter'] = parameter #should be from parent
                childDict['position'] = int(p)
                childDict['description'] = node_info[0]
                childDict['range'] = node_info[1]
                childDict['scale_or_enum'] = node_info[2]
                childDict['ttl'] = 1.0
                if len(node_info) > 4:
                    childDict['readwrite'] = node_info[4]
                else:
                    childDict['readwrite'] = 0
                apply(node_info[3], (childDict,))
    else:
        raise ENoSuchName('TCS type', str(tcstype), str(type(tcstype)))
    
