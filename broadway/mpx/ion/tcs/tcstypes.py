def degrees_F(config):
    answer = value.PValue()
    answer.configure(config)
    if config['scale_or_enum']:
        sl, sh = config['scale_or_enum']
        rl, rh = config['range']
        calc_config = {}
        calc_config['name'] = 'F'
        calc_config['parent'] = answer
        calc_config['multiplier'] = (float(sh) + 1.0 - float(sl))/(float(rh) + 1.0 - float(rl))
        calc_config['offset'] = float(sl) 
        calc = LinearAdjustor()
        calc.configure(calc_config)


